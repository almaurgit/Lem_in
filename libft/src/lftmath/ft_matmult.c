#include "libft.h"

void	ft_matmult(t_matrix *a, float k)
{
	int	i;
	int	j;

	i = 0;
	while (i < a->h)
	{
		j = 0;
		while (j < a->w)
		{
			a->m[i][j] *= k;
			j++;
		}
		i++;
	}
}
