int	ft_strequ(const char *s1, const char *s2)
{
	int	k;

	if (!s1 || !s2)
		return (0);
	k = 0;
	while (s1[k] == s2[k] && s1[k] && s2[k])
		k++;
	return (s1[k] == s2[k]);
}
