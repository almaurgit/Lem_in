#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	while (*s1 && *s2 && *s1 == *s2 && n)
	{
		s1++;
		s2++;
		n--;
	}
	if (!n)
	{
		s1--;
		s2--;
	}
	return ((t_uchar)(*s1) - (t_uchar)(*s2));
}
