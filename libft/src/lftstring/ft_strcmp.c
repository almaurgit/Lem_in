#include "libft_types.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	while (*s1 && *s2 && *s1 == *s2)
	{
		s1++;
		s2++;
	}
	return ((t_uchar)(*s1) - (t_uchar)(*s2));
}
