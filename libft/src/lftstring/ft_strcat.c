char	*ft_strcat(char *s1, const char *s2)
{
	int	k;
	int	l;

	k = 0;
	l = 0;
	while (s1[k])
		k++;
	while (s2[l])
		s1[k++] = s2[l++];
	s1[k] = '\0';
	return (s1);
}
